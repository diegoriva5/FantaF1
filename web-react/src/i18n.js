function getNestedValue(source, key) {
  return String(key || "")
    .split(".")
    .reduce((current, token) => (current && token in current ? current[token] : undefined), source);
}

function interpolate(template, params) {
  if (typeof template !== "string") return template;

  return template.replace(/\{\{\s*(\w+)\s*\}\}/g, (_, token) => {
    const value = params?.[token];
    return value === undefined || value === null ? "" : String(value);
  });
}

export function isSupportedLanguage(language) {
  return SUPPORTED_LANGUAGES.includes(language);
}

export function getDefaultLanguage() {
  return DEFAULT_LANGUAGE;
}

export function getDateLocale(language) {
  return DATE_LOCALE_BY_LANGUAGE[language] || DATE_LOCALE_BY_LANGUAGE[DEFAULT_LANGUAGE];
}

export function translate(language, key, params) {
  const normalizedLanguage = isSupportedLanguage(language) ? language : DEFAULT_LANGUAGE;
  const localizedTemplate = getNestedValue(TRANSLATIONS[normalizedLanguage], key);
  const fallbackTemplate = getNestedValue(TRANSLATIONS[DEFAULT_LANGUAGE], key);
  const template = localizedTemplate ?? fallbackTemplate ?? key;
  return interpolate(template, params);
}

const I18nContext = createContext({
  language: DEFAULT_LANGUAGE,
  setLanguage: () => {},
  dateLocale: DATE_LOCALE_BY_LANGUAGE[DEFAULT_LANGUAGE],
  t: (key) => key,
});

export function I18nProvider({ language, setLanguage, children }) {
  const dateLocale = getDateLocale(language);

  const t = useCallback(
    (key, params) => translate(language, key, params),
    [language],
  );

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      dateLocale,
      t,
    }),
    [dateLocale, language, setLanguage, t],
  );

  return createElement(I18nContext.Provider, { value }, children);
}

export function useI18n() {
  return useContext(I18nContext);
}
